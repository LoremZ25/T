public class Gerente {
    private int NomGerente;
    private int ApeGerente;
    private double Telefono;
    private String correo;
    public int getNomGerente() {
        return NomGerente;
    }
    public void setNomGerente(int nomGerente) {
        NomGerente = nomGerente;
    }
    public int getApeGerente() {
        return ApeGerente;
    }
    public void setApeGerente(int apeGerente) {
        ApeGerente = apeGerente;
    }
    public double getTelefono() {
        return Telefono;
    }
    public void setTelefono(double telefono) {
        Telefono = telefono;
    }
    public String getCorreo() {
        return correo;
    }
    public void setCorreo(String correo) {
        this.correo = correo;
    }
    

}
